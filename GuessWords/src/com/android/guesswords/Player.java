package com.android.guesswords;

public class Player {
	
	private String name;
	
	private int rightTimes;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getRightTimes() {
		return rightTimes;
	}

	public void setRightTimes(int rightTimes) {
		this.rightTimes = rightTimes;
	}

}
